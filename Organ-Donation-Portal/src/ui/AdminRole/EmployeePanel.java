    private void btnCreateEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateEmpActionPerformed

        Organisation org = (Organisation) cbEmpOrganisationComboBox.getSelectedItem();

        String name = txtEmpName.getText();
        
        if(org.getName().equals("Volunteer Organization")){
             org.getEmployeeDirectory().createVolunteer(name);
             populateTable(org);
             return;
        }
        
        org.getEmployeeDirectory().createEmployee(name);
        populateTable(org);

    }//GEN-LAST:event_btnCreateEmpActionPerformed

