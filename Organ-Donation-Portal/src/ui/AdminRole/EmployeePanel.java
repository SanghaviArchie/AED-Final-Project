    public void autoPopulateCbOrganisation(){
        cbOrganisation.removeAllItems();
        
        for (Organisation org : orgDir.getOrgList()){
            cbOrganisation.addItem(org);
        }
    }

    public void autoPopulateCbOrganizationEmp(){
        cbEmpOrganisationComboBox.removeAllItems();
        
        for (Organisation org : orgDir.getOrgList()){
            cbEmpOrganisationComboBox.addItem(org);
        }
    }

    private void populateTable(Organisation org){
        DefaultTableModel mdl = (DefaultTableModel) tblOrganisation.getModel();
        
        mdl.setRowCount(0);
        
        for (Employee employee : org.getEmployeeDirectory().getEmployeeList()){
            Object[] row = new Object[2];
            row[0] = employee.getId();
            row[1] = employee.getName();
            mdl.addRow(row);
        }
    }
