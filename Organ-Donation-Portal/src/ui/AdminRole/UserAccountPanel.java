    private void btnCreateUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateUserActionPerformed
        String userName = txtUsername.getText();
        String pwd = txtPassword.getText();
        Organisation org = (Organisation) cbOrganisation.getSelectedItem();
        
        Role role = (Role) cbRole.getSelectedItem();
        if(cbEmployee.getSelectedItem()==null){
            JOptionPane.showMessageDialog(null, "Please create a new employee or select a new one!");
            return;
        }
        Employee employee = (Employee) cbEmployee.getSelectedItem();
        if(userName.equals("") || userName==null || pwd.equals("") || pwd==null){
            JOptionPane.showMessageDialog(null, "Please enter username and password for login");
            return;
        }
        
       for(UserAccount ua:org.getUserAccountDirectory().getUserAccountList()){
            if(ua.getUsername().equals(userName)){
                JOptionPane.showMessageDialog(null, "This username already exists..!!! Please enter a different username");
                return;
            }
        }
        
        
        for(UserAccount ua:org.getUserAccountDirectory().getUserAccountList()){
            if(ua.getEmployee() == employee){
                JOptionPane.showMessageDialog(null, "This employee already exists");
                return;
            }
        }
        if(!checkPassword(pwd)){
            JOptionPane.showMessageDialog(null,"The password must contain alteast 1 uppercase character, 1 lowercase character, 1 digit and 1 special character ($,*,#,&) and atleast 6 characters.");
            return;
        }
        
        if(org.getName().equals("Volunteer Organization")){
            Volunteer volunteer = (Volunteer) employee;
            org.getUserAccountDirectory().createUserAccount(userName, pwd, volunteer, role);
           
        } else {
            
             org.getUserAccountDirectory().createUserAccount(userName, pwd, employee, role);
             
        }
        

//        organization.getUserAccountDirectory().createUserAccount(userName, password, employee, role);
        
        txtUsername.setText("");
        txtPassword.setText("");
        popData();
    }//GEN-LAST:event_btnCreateUserActionPerformed

