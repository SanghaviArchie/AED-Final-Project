    private boolean checkPassword(String input){
        Pattern p = Pattern.compile("^(?=.*[A-Z])(?=.*[a-z])(?=.*\\d)(?=.*[$#&])[A-Za-z\\d$#*&]{6,}$");
        Matcher m = p.matcher(input);
        return m.matches();
    }    
    
