    private void popTable() {
        DefaultTableModel model = (DefaultTableModel) enterprisetable.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()) {
                    Object[] row = new Object[3];
                    row[0] = enterprise.getName();
                    row[1] = network.getName();
                    row[2] = userAccount.getUsername();

                    model.addRow(row);
                }
            }
        }
    }
    private void populateNetworkCB(){
        cbnetwork.removeAllItems();
        
        for (Network network : system.getNetworkList()){
            cbnetwork.addItem(network);
        }
    }
    private void cbAutoPopulateEnterprise(Network network){
        cbenterprise.removeAllItems();
        
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
            cbenterprise.addItem(enterprise);
        }
        
    }

