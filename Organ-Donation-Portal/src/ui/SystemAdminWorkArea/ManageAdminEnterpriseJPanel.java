    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed

        Enterprise enterprise = (Enterprise) cbenterprise.getSelectedItem();

        String uname = txtusername.getText();
        String pwd = String.valueOf(txtpassword.getPassword());
        String Name = txtname.getText();
        if(uname.equals("") || uname == null || pwd.equals("") || pwd == null){
            JOptionPane.showMessageDialog(null, "Enter details to login");
            return;
        }
        if(Name.equals("") || Name == null){
            JOptionPane.showMessageDialog(null, "Enter ");
            return;
        }
        for(Employee e: enterprise.getEmployeeDirectory().getEmployeeList()){
            if(e.getName().equalsIgnoreCase(Name)){
                JOptionPane.showMessageDialog(null, "This employee already exists.");
                return;
            }
        }
        //        if(!passwordPatternCorrect(password)){
            //            JOptionPane.showMessageDialog(null,"The password must contain alteast 1 uppercase character, 1 lowercase character, 1 digit and 1 special character ($,*,#,&) and atleast 6 characters.");
            //            return;
            //        }
        Employee employee = enterprise.getEmployeeDirectory().createEmployee(Name);

        enterprise.getUserAccountDirectory().createUserAccount(uname, pwd, employee, new AdminRole());
        popTable();

        txtusername.setText("");
        txtpassword.setText("");
        txtname.setText("");

    }//GEN-LAST:event_btnsubmitActionPerformed

