    private boolean correctpassword(String input){
        Pattern pat = Pattern.compile("^(?=.*[A-Z])(?=.*[a-z])(?=.*\\d)(?=.*[$#&])[A-Za-z\\d$#*&]{6,}$");
//        Pattern p = Pattern.compile("^(?=.*\\w)(?=.*[$#&*])[A-Za-z\\d$#*&]{6,}$");
        Matcher m = pat.matcher(input);
        return m.matches();
    }
    
